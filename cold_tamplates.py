"""
cold_templates.py — шаблонные сообщения и логика ветвления для холодных контактов.
Gemini убран полностью. Диалог: opener → ветка по ответу → питч → молчим.
"""

# ── Шаблоны ────────────────────────────────────────────────────────

OPENER = (
    "Добрый день, видел ваш пост в группе по аренде. Вы риэлтор или собственник?"
)

PITCH_REALTOR = (
    "Пишу потому что знаю как избавить вас от вечного поиска клиентов — "
    "зайдите в @lead_vitrina_bot, нажмите старт и выберите город. "
    "Следующие три дня в этот бот вам будут поступать все заявки арендаторов "
    "из 150+ групп по Грузии. Бесплатно"
)

PITCH_OWNER = (
    "Пишу потому что знаю как найти вам арендатора без агентов и комиссий — "
    "зайдите в @lead_vitrina_bot, нажмите старт и выберите город. "
    "Следующие три дня в этот бот вам будут поступать все заявки арендаторов "
    "из 150+ групп по Грузии. Бесплатно"
)

PITCH_NEUTRAL = (
    "Пишу потому что знаю как закрыть вопрос с поиском арендаторов — "
    "зайдите в @lead_vitrina_bot, нажмите старт и выберите город. "
    "Следующие три дня вам будут поступать все заявки из 150+ групп по Грузии. Бесплатно"
)

CLARIFY = (
    "Вы работаете с арендой — риэлтор или сдаёте своё?"
)

# ── Ключевые слова для определения типа ───────────────────────────

_REALTOR_KEYWORDS = [
    "риэлтор", "риелтор", "агент", "агентство", "брокер",
    "работаю", "занимаюсь", "продаю", "сдаю чужие",
]

_OWNER_KEYWORDS = [
    "собственник", "своя", "своё", "моя", "моё", "своих",
    "сдаю своё", "сдаю свою", "хозяин", "владелец",
    "своя квартира", "своё жильё",
]


def classify_reply(text: str) -> str:
    """
    Определяет тип ответа лида по ключевым словам.
    Возвращает: 'realtor' | 'owner' | 'neutral'
    """
    t = text.lower()
    if any(kw in t for kw in _REALTOR_KEYWORDS):
        return "realtor"
    if any(kw in t for kw in _OWNER_KEYWORDS):
        return "owner"
    return "neutral"


def get_pitch(reply_type: str) -> str:
    """Возвращает нужный питч по типу."""
    if reply_type == "realtor":
        return PITCH_REALTOR
    if reply_type == "owner":
        return PITCH_OWNER
    return PITCH_NEUTRAL


