import logging
import google.generativeai as genai
from config import Config

logger = logging.getLogger(__name__)

genai.configure(api_key=Config.GEMINI_API_KEY)

# ------------------------------------------------------------------ #
#  Основной промпт (диалог с новым риэлтором)                         #
# ------------------------------------------------------------------ #

SALES_PROMPT = """Ты — Никита, ищешь квартиру в Батуми в аренду. Тебе написал риэлтор.

Параллельно ты развиваешь сервис «Лид-витрина» — Telegram-бот @lead_vitrina_bot, 
который мониторит 150+ групп по недвижимости Батуми и присылает 
релевантные запросы от клиентов в реальном времени. 
Риэлтор получает готовых покупателей и арендаторов вместо 
ручного мониторинга десятков групп.

ВОРОНКА (строго по этапам):
1. Ответь на суть сообщения риэлтора — не игнорируй его предложение
2. Через 1-2 реплики спроси как он ищет клиентов
3. Когда расскажет — кратко объясни Лид-витрину (одно сообщение)
4. Предложи 3-дневный БЕСПЛАТНЫЙ триал — это главный оффер
   Когда риэлтор соглашается на триал — скажи: 
   «Отлично, вот ссылка: @lead_vitrina_bot — подпишись там и начнёшь получать лиды»
   НЕ говори «сейчас подключу» или «включу триал» — только давай ссылку
5. На любое возражение — триал снимает риск, пробовать нечего терять
6. Если спрашивает цену или хочет подробности — скажи «уточню детали и напишу»

ОТВЕТЫ НА ЧАСТЫЕ СИТУАЦИИ:
- «клиентов хватает» → «триал бесплатный, в несезон пригодится»
- «это спам?» → «парсит публичные посты, как агрегатор в реальном времени»
- «покажи пример» → «лучше вживую — вот ссылка @lead_vitrina_bot, сам увидишь»
- «сколько стоит?» → «уточню детали и напишу» [СТОП — сигнал для владельца]
- «не интересно» → вежливо завершить, не давить

СТИЛЬ:
- Короткие сообщения, живой русский язык
- Не продавать в лоб с первого сообщения
- Никогда не называть себя ботом или ИИ
- Не придумывать цифры и гарантии
- Если спрашивают «ты бот?» — уходи от темы естественно: 
  «да нет, просто параллельно несколько дел веду» и продолжай разговор
- Фраза «уточню реквизиты/детали и напишу» = сигнал для владельца
"""

# ------------------------------------------------------------------ #
#  Промпт для дожима в конце триала (за сутки до окончания)           #
# ------------------------------------------------------------------ #

TRIAL_CLOSING_PROMPT = """Ты — Никита. Ты ведёшь сервис «Лид-витрина» (@lead_vitrina_bot).

Риэлтор попробовал 3-дневный бесплатный триал и завтра он заканчивается.
Тебе нужно написать ему ОДНО сообщение с предложением купить доступ.

УСЛОВИЯ:
- Обычная цена: 24$
- Специальная цена для первой покупки: 19$ (скидка действует только пока идёт триал — то есть до конца сегодняшнего дня)
- Давление не через страх, а через ценность: он уже видел лиды, жалко терять поток

СТРУКТУРА сообщения:
1. Напомни что триал заканчивается завтра
2. Спроси как впечатления / что пришло
3. Скажи что для первого подключения есть цена 19$ вместо 24$ — но только до конца триала
4. Мягко предложи написать если берёт — оформишь

СТИЛЬ:
- Живой, не рекламный
- Одно сообщение, не длинное
- Не называть себя ботом или ИИ
- Никаких восклицательных знаков в ряд
"""

# ------------------------------------------------------------------ #
#  Промпт для дожима перед концом платной подписки (за 3 дня)         #
# ------------------------------------------------------------------ #

RENEWAL_CLOSING_PROMPT = """Ты — Никита. Ты ведёшь сервис «Лид-витрина» (@lead_vitrina_bot).

Риэлтор — платящий клиент, его подписка истекает через 3 дня.
Тебе нужно написать ему ОДНО сообщение с предложением продлить.

УСЛОВИЯ:
- Цена продления: 24$
- Акцент на непрерывности: если не продлит — пропустит лиды

СТРУКТУРА:
1. Напомни что подписка заканчивается через 3 дня
2. Предложи продлить заранее чтобы не было перерыва
3. Скажи написать тебе — оформишь

СТИЛЬ:
- Коротко, по-деловому, без давления
- Живой язык
- Не называть себя ботом
"""

# Фразы-триггеры передачи владельцу
OWNER_SIGNAL_PHRASES = [
    "уточню реквизиты",
    "уточню детали",
    "уточню и напишу",
]

# Фразы-триггеры что ссылка на бота была дана (триал начат)
TRIAL_LINK_PHRASES = [
    "@lead_vitrina_bot",
    "lead_vitrina_bot",
]


class GeminiClient:
    def __init__(self):
        self._sales_model = genai.GenerativeModel(
            model_name=Config.GEMINI_MODEL,
            system_instruction=SALES_PROMPT,
        )
        # История per user_id: список {role, parts}
        self._histories: dict[int, list] = {}

    def get_history(self, user_id: int) -> list:
        return self._histories.get(user_id, [])

    def clear_history(self, user_id: int):
        self._histories.pop(user_id, None)

    async def chat(self, user_id: int, message: str) -> tuple[str, bool, bool]:
        """
        Основной диалог с риэлтором.
        Возвращает: (reply, needs_owner_takeover, trial_link_sent)
        """
        history = self.get_history(user_id)
        session = self._sales_model.start_chat(history=history)
        response = await session.send_message_async(message)
        reply = response.text.strip()
        self._histories[user_id] = list(session.history)

        needs_takeover = any(p in reply.lower() for p in OWNER_SIGNAL_PHRASES)
        trial_link_sent = any(p in reply for p in TRIAL_LINK_PHRASES)

        return reply, needs_takeover, trial_link_sent

    async def generate_trial_closing(self, username: str) -> str:
        """Сгенерировать сообщение-дожим для клиента в конце триала."""
        model = genai.GenerativeModel(
            model_name=Config.GEMINI_MODEL,
            system_instruction=TRIAL_CLOSING_PROMPT,
        )
        prompt = f"Напиши сообщение риэлтору {username}. Триал заканчивается завтра."
        try:
            response = await model.generate_content_async(prompt)
            return response.text.strip()
        except Exception as e:
            logger.error(f"trial_closing generation error: {e}")
            return (
                f"Привет! Напомню — завтра заканчивается твой триал в @lead_vitrina_bot. "
                f"Как впечатления, что приходило?\n\n"
                f"Если понравилось — для первого подключения есть цена 19$ вместо 24$, "
                f"но только до конца триала. Напиши если берёшь."
            )

    async def generate_renewal_closing(self, username: str, days_left: int) -> str:
        """Сгенерировать сообщение-дожим для платного клиента перед концом подписки."""
        model = genai.GenerativeModel(
            model_name=Config.GEMINI_MODEL,
            system_instruction=RENEWAL_CLOSING_PROMPT,
        )
        prompt = (
            f"Напиши сообщение риэлтору {username}. "
            f"Подписка заканчивается через {days_left} дня."
        )
        try:
            response = await model.generate_content_async(prompt)
            return response.text.strip()
        except Exception as e:
            logger.error(f"renewal_closing generation error: {e}")
            return (
                f"Привет! Подписка на @lead_vitrina_bot заканчивается через {days_left} дня. "
                f"Продлить можно за 24$ — напиши, оформлю без перерыва."
            )


gemini = GeminiClient()
